// Q9 demonstrate the use of switch case statement in javascript
let age = prompt("enter your age")
switch(age){
  case "15":
    alert("your age is 15");
    break;
  case "16" :
    alert("your age is 16");
    break;
  case "17":
    alert("your age is 17");
    break;
  default:
    alert("your age is not special")
}